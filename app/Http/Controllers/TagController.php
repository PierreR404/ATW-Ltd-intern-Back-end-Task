     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        //
        $fields = $request->validate([
            'name'=>'unique:tags,name'
        ]);
        $tag = Tag::find($id);
        $tag->name =$fields['name'];
